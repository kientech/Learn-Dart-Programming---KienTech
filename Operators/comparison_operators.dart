void main() {
  int x = 10;
  int y = 20;

  print('x == y: ${x == y}');  // Bằng
  print('x != y: ${x != y}');  // Không bằng
  print('x > y: ${x > y}');    // Lớn hơn
  print('x < y: ${x < y}');    // Nhỏ hơn
  print('x >= y: ${x >= y}');  // Lớn hơn hoặc bằng
  print('x <= y: ${x <= y}');  // Nhỏ hơn hoặc bằng
}